  var I18N = { zh: merge(copy(I18N_ZH), PXMM_ZH), en: merge(copy(I18N_EN), PXMM_EN) };
  for (var k in I18N.zh) {
    if (I18N.en[k] == null) I18N.en[k] = I18N.zh[k];
  }

  function t(k) {
    var L = getLang() === "en" ? "en" : "zh";
    var pack = I18N[L] || I18N.zh;
    return pack.hasOwnProperty(k) ? pack[k] : I18N.zh[k] != null ? I18N.zh[k] : k;
  }
  function getLang() {
    return localStorage.getItem(LS_KEY) === "en" ? "en" : "zh";
  }
  function setLang(l) {
    if (l === "en" || l === "zh") localStorage.setItem(LS_KEY, l);
  }
  function targetDisplayName(internal) {
    if (getLang() !== "en") return internal;
    return Object.prototype.hasOwnProperty.call(TARGET_LABEL_EN, internal) ? TARGET_LABEL_EN[internal] : internal;
  }

  function applyDataI18n(root) {
    root = root || document.body;
    if (!root) return;
    [].forEach.call(root.querySelectorAll("[data-i18n]"), function (el) {
      var k = el.getAttribute("data-i18n");
      if (k) el.textContent = t(k);
    });
    [].forEach.call(root.querySelectorAll("[data-i18n-html]"), function (el) {
      var k = el.getAttribute("data-i18n-html");
      if (k) el.innerHTML = t(k);
    });
    [].forEach.call(root.querySelectorAll("[data-i18n-placeholder]"), function (el) {
      var k = el.getAttribute("data-i18n-placeholder");
      if (k) el.setAttribute("placeholder", t(k));
    });
    [].forEach.call(root.querySelectorAll("[data-i18n-title-attr]"), function (el) {
      var k = el.getAttribute("data-i18n-title-attr");
      if (k) el.setAttribute("title", t(k));
    });
  }

  var _langBound = false;
  function wireLangButton() {
    if (_langBound) return;
    _langBound = true;
    var b = document.getElementById("btn-lang");
    if (!b) return;
    b.addEventListener("click", function () {
      setLang(getLang() === "zh" ? "en" : "zh");
      refreshAll();
    });
  }

  function refreshAll() {
    var L = getLang();
    document.documentElement.setAttribute("lang", L === "en" ? "en" : "zh-CN");
    if (document.body) document.body.classList.toggle("lang-en", L === "en");
    var docKey = document.body && document.body.getAttribute("data-i18n-doc");
    if (docKey) document.title = t(docKey);
    applyDataI18n(document.body);
    var btn = document.getElementById("btn-lang");
    if (btn) {
      btn.textContent = L === "zh" ? t("langToEn") : t("langToZh");
      btn.setAttribute("title", btn.textContent);
    }
    try {
      document.dispatchEvent(new CustomEvent("threshold:locale", { detail: { lang: L } }));
    } catch (e) {}
  }

